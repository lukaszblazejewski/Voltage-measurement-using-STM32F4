void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
	int Data_in = 0;
	int Data_Out = 0;
	static int Integrator_1 = 0;
	static int Integrator_2 = 0;
	static int Integrator_3 = 0;
	static int dec_clk = 0;
	static int word_count = 0;
	static int diff_1 = 0;
	static int diff_2 = 0;
	static int diff_3 = 0;
	static int diff_1_d = 0;
	static int diff_2_d = 0;
	static int Integrator_3_d2 = 0;
	uint16_t size = 0;
	uint8_t data[50] = {0};
	uint16_t Data_UART = 0;

	if(htim->Instance == TIM1)
	{
		Data_in = HAL_GPIO_ReadPin(DAT_GPIO_Port, DAT_Pin);		// Przypisanie wartosci bitu wejsciowego

			/////////////////////////// Filtr sinc3 //////////////////////////////////
			////////////////////////////////////////// Sekcja ró¿nicowania	(Differentiation stage)
			if(dec_clk == 1)
			{
				diff_3 = diff_2 - diff_2_d;
				diff_2_d = diff_2;
				diff_2 = diff_1 - diff_1_d;
				diff_1_d = diff_1;
				diff_1 = Integrator_3 - Integrator_3_d2;
				Integrator_3_d2 = Integrator_3;
				Data_Out = (diff_3 >> 8);
			////////////////////////////////////////// Wystawienie 16 bitowego slowa wyjsciowego

				if(Data_Out == 65536)
					Data_Out = 65535;
				Data_UART = Data_Out;
				size = sprintf(data, "%d\n\r", Data_UART);
				HAL_UART_Transmit_IT(&huart6, data, size);	//Wys³anie danych do komputera
				dec_clk = 0;
				TIM1 -> PSC = 31;	// Zmiana czêstotliwoci sygna³u zegarowego
			}

			if(word_count == 255)	// Podzia³ czêstotliwoci
				word_count = 0;
			else
				word_count++;
			if(word_count == 128)
			{
				dec_clk = 1;		 // Wykonanie sekcji ró¿nicowania i wys³ani danych
				TIM1 -> PSC = 699; // Zmiana czêstotliwoci sygna³u zegarowego
			}

			/////////////////////////////////////////// Rozpoczecie akumulacji (Integrator stage)

			Integrator_3 = Integrator_3 + Integrator_2;	//Blok trzeci sekcji sumowania
			Integrator_2 = Integrator_2 + Integrator_1; //Blok drugi sekcji sumowania
			Integrator_1 = Integrator_1 + Data_in;      //Blok pierwszy sekcji sumowania
	}

}

